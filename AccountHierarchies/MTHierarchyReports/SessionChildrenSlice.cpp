STDMETHODIMP CSessionChildrenSlice::FromString(ISliceLexer* apLexer)
{
	if (!apLexer)
		return E_POINTER;
	try
	{
		MTHIERARCHYREPORTSLib::ISliceLexerPtr pLexer(apLexer);
		if(SliceToken::SESSIONCHILDREN != pLexer->GetNextToken()) 
		{
			char buf [512];
			_snprintf_s("Parse Error: Expected token %s", 512, 512, (const char *)SliceToken::SESSIONCHILDREN);
			return Error(buf);
		}
		int scanned;
		scanned = swscanf((const wchar_t *) pLexer->GetNextToken(), L"%I64d", &mParentID);
		if (scanned != 1) return Error("Parse Error: expected integer");
	}
	catch(_com_error & e)
	{
		return returnHierarchyReportError(e);
	}
	return S_OK;
}

STDMETHODIMP CSessionChildrenSlice::Equals(IViewSlice* apSlice, VARIANT_BOOL *pVal)
{
	if(!pVal)
		return E_POINTER;
	else
		*pVal = VARIANT_FALSE;

	try
	{
		// Cast to session children slice
		MTHIERARCHYREPORTSLib::IViewSlicePtr pSlice(apSlice);
		if(pSlice)
		{
			MTHIERARCHYREPORTSLib::ISessionChildrenSlicePtr pCast(pSlice);
			if(NULL != pCast &&
         pCast->ParentID == mParentID)
			{
				*pVal = VARIANT_TRUE;
			}
		}
	}
	catch(_com_error & e)
	{
		return returnHierarchyReportError(e);
	}
	return S_OK;
}

STDMETHODIMP CSessionChildrenSlice::Clone(IViewSlice* *pVal)
{
	if(!pVal)
		return E_POINTER;
	else
		*pVal = NULL;

	try
	{
		MTHIERARCHYREPORTSLib::ISessionChildrenSlicePtr This(this);
		MTHIERARCHYREPORTSLib::ISessionChildrenSlicePtr clone(__uuidof(MTHIERARCHYREPORTSLib::SessionChildrenSlice));
		clone->ParentID = This->ParentID;
		*pVal = reinterpret_cast<ISessionChildrenSlice *> (clone.Detach());
	}
	catch(_com_error & e)
	{
		return returnHierarchyReportError(e);
	}
	return S_OK;
}

