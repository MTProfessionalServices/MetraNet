  /// acSecurityVerifier base class
  /// </summary>
  class SecurityVerifier : Verifier
  {
    public string HASH { get; set; }
    public SecurityVerifier(string name, string hash)
      : base(name)
    {
      if (hash == string.Empty)
        throw new Exception("hash is empty");
      HASH = hash;
    }
    public override bool Verify()
    {
      Log.Error(CODE.__FUNCTION__ + " should not be called. No implementation");
      return false;
    }
