  #region Security Token Verification
  /// <summary>
  /// Token verifier
  /// </summary>
  class TokenVerifier : SecurityVerifier
  {
    public string TOKEN { get; set; }

    public TokenVerifier(string name, string token, ref string systemToken)
      : base(name, token)
    {
      if (systemToken == string.Empty)
        throw new Exception("systemToken is empty");
      TOKEN = systemToken;
    }
    public override bool Verify()
    {
      Log.Debug(CODE.__FUNCTION__);
      return false;
    }
  } 
