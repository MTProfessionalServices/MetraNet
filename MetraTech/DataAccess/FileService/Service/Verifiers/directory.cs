  #region Simple Directory acVerifier
  /// <summary>
  /// cDirectoryVerifier defines what it means to verify a directory. 
  /// For now it just checks the existance
  /// </summary>
  class cDirectoryVerifier : acVerifier
  {
    public cDirectoryVerifier(string value)
      : base(value)
    {
      Name = "cDirectoryVerifier";
    }

    public override bool Verify()
    {
      Log.Debug(CODE.__FUNCTION__);
      if (Directory.Exists(VALUE))
        return true;
      return false;
    }
  } 
