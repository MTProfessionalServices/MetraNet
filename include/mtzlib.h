#ifndef _MTZLIB_H
#define _MTZLIB_H

#define ZLIB_DLL
#include <zlib.h>

// light wrapper around zlib

class MTZLib
{
public:

	// return a buffer size to use when compressing data of the
	// given size.
	static int RecommendCompressedBufferSize(int aUncompressedSize);

	// compress a block of data.
	/*
		from zlib.h:

     The compression level must be Z_DEFAULT_COMPRESSION, or between 0 and 9:
   1 gives best speed, 9 gives best compression, 0 gives no compression at
   all (the input data is simply copied a block at a time).
   Z_DEFAULT_COMPRESSION requests a default compromise between speed and
   compression (currently equivalent to level 6).

     Compresses the source buffer into the destination buffer. The level
   parameter has the same meaning as in deflateInit.  sourceLen is the byte
   length of the source buffer. Upon entry, destLen is the total size of the
   destination buffer, which must be at least 0.1% larger than sourceLen plus
   12 bytes. Upon exit, destLen is the actual size of the compressed buffer.

     compress2 returns Z_OK if success, Z_MEM_ERROR if there was not enough
   memory, Z_BUF_ERROR if there was not enough room in the output buffer,
   Z_STREAM_ERROR if the level parameter is invalid.
	*/

	static int Compress(unsigned char * apDest, unsigned long * apDestLen,
											const unsigned char * apSource, unsigned long aSourceLen,
											int aLevel = Z_DEFAULT_COMPRESSION);


	// decompress a block of data
	/*
		from zlib.h:

     Decompresses the source buffer into the destination buffer.  sourceLen is
   the byte length of the source buffer. Upon entry, destLen is the total
   size of the destination buffer, which must be large enough to hold the
   entire uncompressed data. (The size of the uncompressed data must have
   been saved previously by the compressor and transmitted to the decompressor
   by some mechanism outside the scope of this compression library.)
   Upon exit, destLen is the actual size of the compressed buffer.
     This function can be used to decompress a whole file at once if the
   input file is mmap'ed.

     uncompress returns Z_OK if success, Z_MEM_ERROR if there was not
   enough memory, Z_BUF_ERROR if there was not enough room in the output
   buffer, or Z_DATA_ERROR if the input data was corrupted.
	*/

	static int Uncompress(unsigned char * apDest, unsigned long * apDestLen,
												const unsigned char * apSource,
												unsigned long aSourceLen);

};

#endif /* _MTZLIB_H */
